public class SquareOfNumber_Range {

    public static void main(String[] args) {
        int start=30;
        int end=60;
        int square;
        int i;
        for(i=start+1; i<end; i++)
        {
            square=i*i;
            System.out.print("Square of Range Element is "+ square);
        }
        
    }
}
